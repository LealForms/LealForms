namespace LealForms.Enums.Checkbox;

/// <summary>
/// Specifies the different types of characters that can be used for checkboxes.
/// </summary>
public enum CheckboxCharacterType
{
    /// <summary>
    /// No character.
    /// </summary>
    None,

    /// <summary>
    /// A check mark character.
    /// </summary>
    CheckMark,

    /// <summary>
    /// A cross mark character.
    /// </summary>
    CrossMark,

    /// <summary>
    /// A dot mark character.
    /// </summary>
    DotMark,

    /// <summary>
    /// A star mark character.
    /// </summary>
    StarMark,

    /// <summary>
    /// A heart mark character.
    /// </summary>
    HeartMark,

    /// <summary>
    /// A square mark character.
    /// </summary>
    SquareMark,

    /// <summary>
    /// A circle mark character.
    /// </summary>
    CircleMark,

    /// <summary>
    /// A triangle mark character.
    /// </summary>
    TriangleMark,
}