namespace LForms.Enums.MessageBox;

/// <summary>
/// Specifies the icon type to display in a message box.
/// </summary>
public enum IconType
{
    /// <summary>
    /// No icon is displayed.
    /// </summary>
    None,

    /// <summary>
    /// An information icon is displayed.
    /// </summary>
    Information,

    /// <summary>
    /// A warning icon is displayed.
    /// </summary>
    Warning,

    /// <summary>
    /// An error icon is displayed.
    /// </summary>
    Error,

    /// <summary>
    /// A question icon is displayed.
    /// </summary>
    Question,

    /// <summary>
    /// A custom icon is displayed.
    /// </summary>
    Custom
}