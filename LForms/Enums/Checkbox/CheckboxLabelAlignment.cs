namespace LealForms.Enums.Checkbox;

/// <summary>
/// Specifies the alignment options for a checkbox and its associated label.
/// </summary>
public enum CheckboxLabelAlignment
{
    /// <summary>
    /// Checkbox is positioned to the left of the label.
    /// </summary>
    CheckBoxLeftLabelRight,

    /// <summary>
    /// Checkbox is positioned to the right of the label.
    /// </summary>
    CheckBoxRightLabelLeft,

    /// <summary>
    /// Checkbox is positioned above the label.
    /// </summary>
    CheckBoxTopLabelBottom,

    /// <summary>
    /// Checkbox is positioned below the label.
    /// </summary>
    CheckBoxBottomLabelTop
}